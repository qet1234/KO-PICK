  if (p_starts_at at time zone 'Asia/Seoul')::date <> plan_row.reservation_date then raise exception '후보 방문 시간은 예약 계획 날짜와 같아야 합니다.'; end if;
  if p_external_reservation_url is not null and p_external_reservation_url !~* '^https?://[^[:space:]]+$' then raise exception '외부 예약 링크는 올바른 http 또는 https 주소여야 합니다.'; end if;
  select count(*) into candidate_count from public.space_reservation_candidates where plan_id = p_plan_id;
  if candidate_count >= 20 then raise exception '하나의 예약 계획에는 후보를 최대 20곳까지 추가할 수 있습니다.'; end if;
  insert into public.space_reservation_candidates(plan_id, place_source, place_id, place_name, category, address, starts_at, external_reservation_url, created_by)
  values (p_plan_id, left(coalesce(nullif(btrim(p_place_source), ''), 'manual'), 30), nullif(btrim(p_place_id), ''), btrim(p_place_name), nullif(btrim(p_category), ''), nullif(btrim(p_address), ''), p_starts_at, nullif(btrim(p_external_reservation_url), ''), actor)
  returning id into new_id;
  return jsonb_build_object('candidate_id', new_id);
end;
$$;

create or replace function public.toggle_reservation_vote(p_candidate_id uuid)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare actor uuid := auth.uid(); target_space uuid; target_status text; removed integer;
begin
  if actor is null then raise exception '로그인이 필요합니다.'; end if;
  select p.space_id, p.status into target_space, target_status
  from public.space_reservation_candidates c join public.space_reservation_plans p on p.id = c.plan_id
  where c.id = p_candidate_id;
  if target_space is null or not private.is_space_member(target_space, actor) then raise exception '예약 후보를 찾을 수 없습니다.'; end if;
  if target_status not in ('voting', 'ready') then raise exception '예약 요청 전 단계에서만 투표할 수 있습니다.'; end if;
  delete from public.space_reservation_votes where candidate_id = p_candidate_id and user_id = actor;
  get diagnostics removed = row_count;
  if removed = 0 then
    insert into public.space_reservation_votes(candidate_id, user_id) values (p_candidate_id, actor) on conflict do nothing;
    return jsonb_build_object('voted', true);
  end if;
  return jsonb_build_object('voted', false);
end;
$$;

create or replace function public.finalize_reservation_plan(p_plan_id uuid, p_candidate_id uuid)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare actor uuid := auth.uid(); plan_row public.space_reservation_plans%rowtype; candidate_row public.space_reservation_candidates%rowtype;
begin
  if actor is null then raise exception '로그인이 필요합니다.'; end if;
  select * into plan_row from public.space_reservation_plans where id = p_plan_id for update;
  if plan_row.id is null or plan_row.created_by <> actor then raise exception '예약 계획을 만든 대표자만 최종 상태를 변경할 수 있습니다.'; end if;
  if not private.is_space_member(plan_row.space_id, actor) then raise exception '접근할 수 있는 예약 계획을 찾지 못했습니다.'; end if;
  if plan_row.status not in ('voting', 'ready') then raise exception '예약 요청 전 단계에서만 최종 장소를 변경할 수 있습니다.'; end if;
  select * into candidate_row from public.space_reservation_candidates where id = p_candidate_id and plan_id = p_plan_id for update;
  if candidate_row.id is null then raise exception '선택할 예약 후보를 찾을 수 없습니다.'; end if;
  update public.space_reservation_candidates set is_selected = false where plan_id = p_plan_id;
  update public.space_reservation_candidates set is_selected = true where id = p_candidate_id;
  update public.space_reservation_plans set status = 'ready', updated_at = now() where id = p_plan_id;
  insert into public.space_calendar_events(space_id, title, starts_at, all_day, location, note, color, created_by, reservation_plan_id)
  values (plan_row.space_id, '[함께 예약] ' || plan_row.title, candidate_row.starts_at, false, candidate_row.address, '선택 장소: ' || candidate_row.place_name, 'blue', actor, p_plan_id)
  on conflict (reservation_plan_id) where reservation_plan_id is not null
  do update set title = excluded.title, starts_at = excluded.starts_at, location = excluded.location, note = excluded.note, updated_at = now();
  return jsonb_build_object('status', 'ready', 'candidate_id', p_candidate_id, 'calendar_saved', true);
end;
$$;

create or replace function public.update_reservation_status(p_plan_id uuid, p_status text)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare actor uuid := auth.uid(); plan_row public.space_reservation_plans%rowtype; normalized text := lower(btrim(coalesce(p_status, ''))); has_selected boolean;
begin
  if actor is null then raise exception '로그인이 필요합니다.'; end if;
  if normalized not in ('requested', 'confirmed', 'cancelled') then raise exception '예약 요청 중, 예약 확정 또는 취소 상태만 선택할 수 있습니다.'; end if;
  select * into plan_row from public.space_reservation_plans where id = p_plan_id for update;
  if plan_row.id is null or plan_row.created_by <> actor then raise exception '예약 계획을 만든 대표자만 최종 상태를 변경할 수 있습니다.'; end if;
  select exists(select 1 from public.space_reservation_candidates where plan_id = p_plan_id and is_selected) into has_selected;
  if normalized <> 'cancelled' and not has_selected then raise exception '먼저 투표 결과에서 최종 장소를 확정해 주세요.'; end if;
  if not ((normalized = 'requested' and plan_row.status = 'ready') or (normalized = 'confirmed' and plan_row.status = 'requested') or (normalized = 'cancelled' and plan_row.status not in ('confirmed', 'cancelled'))) then
    raise exception '현재 단계에서는 선택한 예약 상태로 변경할 수 없습니다.';
  end if;
  update public.space_reservation_plans set status = normalized, updated_at = now() where id = p_plan_id;
  return jsonb_build_object('status', normalized);
end;
$$;

