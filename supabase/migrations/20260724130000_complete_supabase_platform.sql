create or replace function public.add_reservation_candidate(
  p_plan_id uuid,
  p_place_source text,
  p_place_id text,
  p_place_name text,
  p_category text,
  p_address text,
  p_starts_at timestamptz,
  p_external_reservation_url text
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare actor uuid := auth.uid(); plan_row public.space_reservation_plans%rowtype; new_id uuid; candidate_count integer;
begin
  if actor is null then raise exception '로그인이 필요합니다.'; end if;
  select p.* into plan_row from public.space_reservation_plans p
  where p.id = p_plan_id and private.is_space_member(p.space_id, actor);
  if plan_row.id is null then raise exception '접근할 수 있는 예약 계획을 찾지 못했습니다.'; end if;
  if plan_row.status not in ('voting', 'ready') then raise exception '예약 요청 전 단계에서만 후보를 추가할 수 있습니다.'; end if;
